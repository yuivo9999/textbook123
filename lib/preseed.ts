export async function preseedSampleBookIfNeeded(): Promise<boolean> {
  try {
    await dbInstance.init();
    const existing = await dbInstance.getNovel(SAMPLE_NOVEL_ID);
    if (existing) {
      return false; // Already preseeded
    }

    const novel: Novel = {
      id: SAMPLE_NOVEL_ID,
      title: '时光信使 (The Time Messenger)',
      author: '林墨川',
      coverColor: 'from-violet-100 to-indigo-200 text-indigo-900',
      size: 5420, // approximate size
      wordCount: 2710,
      description: '一个关于时间、物理和亲情的短篇科幻故事。年轻的物理学家叶川在一枚蓝荧吊坠和古老信件的指引下，重回一九九八年，试图解开母亲神秘失踪的谜团...',
      addedAt: Date.now() - 3600000, // 1 hour ago
      lastReadChapterIndex: 0,
      lastReadChapterId: 'chapter_intro',
      progress: 0,
      lastReadAt: Date.now()
    };

    const chapters: Chapter[] = SAMPLE_CHAPTERS_DATA.map((ch, idx) => ({
      id: idx === 0 ? 'chapter_intro' : `chapter_${idx}`,
      novelId: SAMPLE_NOVEL_ID,
      title: ch.title,
      content: ch.content,
      index: idx
    }));

    await dbInstance.saveNovel(novel);
    await dbInstance.bulkSaveChapters(chapters);
    return true;
  } catch (error) {
    console.error('Failed to preseed sample book:', error);
    return false;
  }
}
